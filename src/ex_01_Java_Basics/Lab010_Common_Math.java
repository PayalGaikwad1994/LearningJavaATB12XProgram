package ex_01_Java_Basics;

public class Lab010_Common_Math {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println(2-2);
        System.out.println(2*2);
        System.out.println(2/2);
        System.out.println(2%2);
        System.out.println(2.0/2);
        System.out.println(2.0%2);
        System.out.println(2.0/2.0);
        System.out.println(2.0%2.0);
        System.out.println(5/2);

    }
}
